package day03_sequences_variables;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println(" Hello ");
        System.out.println(" Hello Everyone\nMy name is Rajmi");
        System.out.println(" Hello Everyone\nMy name is Rajmi"); // \n for new line//
        System.out.println(" ");
        System.out.println(" Hello Everyone\tMy name is Rajmi"); // \t for pragraph space //


        System.out.println(" Hello Everyone\n \n\t\t My name is Rajmi");

        System.out.println("\\"); // to print one blackslash)
        System.out.println("/"); // to print 1 front lash type 1 front slash //

        System.out.println("\\\\"); // to print 2 blackslash, need 4blackslashes//
        System.out.println("/"); // to print 2 front lash type 2front slash //
        System.out.println("My name is \'Rajmi\'");
        System.out.println("My Teacher\'s name is Muhtar ");
        System.out.println("Student\'s of Cybertek are \"smart\"");
    }



}
