package day21_loops;

public class DoWhile {
    public static void main(String[] args) {

        boolean result = false;
       //while loop
        while(result){
            System.out.println("Hello World");
        }
        System.out.println("=================================");
        // do while loop
        do{
            System.out.println("Hello World");
        }while (false);

        }



        }


