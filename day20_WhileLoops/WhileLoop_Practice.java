package day20_WhileLoops;

public class WhileLoop_Practice {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.print(i + " ");
        }
        System.out.println();// to append new line
        //while loop

        int num = 1; // innitalization
        while (num <= 10) { //condition
            System.out.print(num + " ");

        num++; // iterator
            //System.out.print(num + " ");


        }
        
    }
}