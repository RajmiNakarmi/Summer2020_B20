package day06_CompatisonOperations;

public class Swap1 {
    public static void main(String[] args) {
        // temporary value assign c
        int a= 10;
        int b = 15;

        int c = b; // c=15; c is temporary variables


        b = a; //b= 10
        a = c; //a = 15

        System.out.println("a = " + a);
        System.out.println(b);



        /* how to swap two variables without temporary variables.

         */
    }
}
