package day05_ArithmeticOperators;

public class ArithmeticOperators {
    public static void main(String[] args){

        System.out.println(20+ 3 -2 +1);
        // 20+3=23
        //23-2+1
        //21 +1 =22
        System.out.println(20*3); //mutlipication

        System.out.println(2+3-1*4);
        //2+3-4
        //5-4 =1
        System.out.println(10/3); //divider
        // int/int = int
        // for eg
        System.out.println(10.0/3); //double/int =double
        System.out.println(10/3.0); // int/double == double
        System.out.println(10.0/3.0);// double/double = double
        System.out.println(10%4); // remainder
        // 10/4 =2
        //10-(4*2)=2
        System.out.println(30%8);// 30/8 =3
        // remainder : 30-(8*3)
        //30-24=6
        System.out.println( 99%4);
        // 99/4 =2
        // 99-(4*24)
        //99-96 =3

        System.out.println( 5+2*4); // 5+8=13
        System.out.println(10/2 -3); //5-3 =2
        System.out.println(8+12*2-4);//8+24-4 = 8+24-4=28
        System.out.println(4+17%2-1);//4+0-1=3
        System.out.println(6-3*2+7-1);//6-6+7-1 =6
    }
}
